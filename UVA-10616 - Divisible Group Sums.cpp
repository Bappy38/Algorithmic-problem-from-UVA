int d,m;
vector<int>val;
ll dp[201][21][21][12];         ///position,carry,mod value,taken coin

ll fun(int i,int carry,int n,int taken)
{
    if(i>=n || taken<=0)
    {
        if((carry+d)%d==0 && taken==0)
            return 1;
        else
            return 0;
    }
    if(dp[i][carry][d][taken] != -1)
        return dp[i][carry][d][taken];
    int ret1=0,ret2=0;
    ret1=fun(i+1,(((carry+val[i])%d)+d)%d,n,taken-1);
    ret2=fun(i+1,carry,n,taken);
    return dp[i][carry][d][taken]=ret1+ret2;
}

int main()
{
    int n,q,temp,i,t=0;
    ll ans;
    while(1)
    {
        val.clear();
        ms(dp,-1);
        scin2(n,q);
        if(n==0 && q==0)
            break;
        for(i=0;i<n;i++)
        {
            scin(temp);
            val.pb(temp);
        }
        pf("SET %d:\n",++t);
        for(i=1;i<=q;i++)
        {
            scin2(d,m);
            ans=fun(0,0,n,m);
            pf("QUERY %d: %lld\n",i,ans);
        }
    }
    return 0;
}

